# Program to demonstrate list, tuple, and dictionary

fruits_list = ["Apple", "Banana", "Cherry"] # List

colors_tuple = ("Red", "Green", "Blue")

# Tuple

student_dict={"Name": "John", "Age": 20}

# Dictionary
print("First fruit:", fruits_list[0])

print("Second color:", colors_tuple[1])

print("Student Name:", student_dict["Name"])

